package javaBasics;

public class Looping {

	public static void main(String[] args) {
		// while loop

		int i = 1;
		while (i <= 10) {
			System.out.println(i);
			i = i + 1;

		}
		System.out.println("*********");
		// for loop

		for (int j = 1; j <= 10; j++) {
			System.out.println(j);

		}
		System.out.println("**************");
		for (int k = 10; k >= 1; k--) {
			System.out.println(k);
		}
	}

}
